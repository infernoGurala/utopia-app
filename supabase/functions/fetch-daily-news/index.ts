/**
 * Fetches raw articles from a News API or uses a fallback curated list
 */
async function fetchRawArticles(activeCategories: any[], debugLogs: string[]) {
  // Execute all 3 layers in parallel
  const [rssArticles, guardianArticles, nytArticles] = await Promise.all([
    fetchRSSLayer(debugLogs).catch((e) => {
      console.error("[Layer 1 RSS] Failed:", e);
      debugLogs.push(`Layer 1 (RSS) failed: ${e.message || e}`);
      return [];
    }),
    fetchGuardianLayer(debugLogs).catch((e) => {
      console.error("[Layer 2 Guardian] Failed:", e);
      debugLogs.push(`Layer 2 (Guardian) failed: ${e.message || e}`);
      return [];
    }),
    fetchNYTLayer(debugLogs).catch((e) => {
      console.error("[Layer 3 NYT] Failed:", e);
      debugLogs.push(`Layer 3 (NYT) failed: ${e.message || e}`);
      return [];
    })
  ]);

  // Log total count per source layer
  console.log(`[Source Layers Fetch Counts]`);
  console.log(`- Layer 1 (RSS): ${rssArticles.length} raw articles`);
  console.log(`- Layer 2 (Guardian): ${guardianArticles.length} raw articles`);
  console.log(`- Layer 3 (NYT): ${nytArticles.length} raw articles`);

  debugLogs.push(`[Source Layer Stats] RSS: ${rssArticles.length}, Guardian: ${guardianArticles.length}, NYT: ${nytArticles.length}`);

  // Merge all results
  const flatArticles = [...rssArticles, ...guardianArticles, ...nytArticles];

  // 1. Calculate source counts before any filtering
  const totalRSS = rssArticles.length;
  const totalGuardian = guardianArticles.length;
  const totalNYT = nytArticles.length;
  const totalBeforeDedup = flatArticles.length;

  // 2. Deduplicate by normalized title
  const seenTitles = new Set<string>();
  const dedupedArticles: any[] = [];
  for (const a of flatArticles) {
    const normalizedTitle = a.title.trim().toLowerCase().replace(/[^a-z0-9]/g, "");
    if (normalizedTitle && !seenTitles.has(normalizedTitle)) {
      seenTitles.add(normalizedTitle);
      dedupedArticles.push(a);
    }
  }
  const totalAfterDedup = dedupedArticles.length;

  // 3. Filter by 4 hours for logging
  const now = new Date();
  const cutoffTime4h = now.getTime() - (4 * 60 * 60 * 1000); // 4 hours in milliseconds
  const articles4h = dedupedArticles.filter((a: any) => {
    if (!a.publishedAt) return false;
    const pubTime = new Date(a.publishedAt).getTime();
    return pubTime >= cutoffTime4h;
  });
  const totalAfter4h = articles4h.length;

  // 4. Relax the age filter from 4 hours to 48 hours (48 hours in milliseconds) for actual curation
  const cutoffTime48h = now.getTime() - (48 * 60 * 60 * 1000);
  const freshArticles = dedupedArticles.filter((a: any) => {
    if (!a.publishedAt) return false;
    const pubTime = new Date(a.publishedAt).getTime();
    return pubTime >= cutoffTime48h;
  });
  const totalAfter48h = freshArticles.length;

  // Print exact logs requested by the user
  console.log(`Total from RSS feeds combined: ${totalRSS}`);
  console.log(`Total from Guardian API: ${totalGuardian}`);
  console.log(`Total from NYT API: ${totalNYT}`);
  console.log(`Total before deduplication: ${totalBeforeDedup}`);
  console.log(`Total after deduplication: ${totalAfterDedup}`);
  console.log(`Total after 4-hour age filter: ${totalAfter4h}`);
  console.log(`Total after 48-hour age filter: ${totalAfter48h}`);

  debugLogs.push(`Total from RSS feeds combined: ${totalRSS}`);
  debugLogs.push(`Total from Guardian API: ${totalGuardian}`);
  debugLogs.push(`Total from NYT API: ${totalNYT}`);
  debugLogs.push(`Total before deduplication: ${totalBeforeDedup}`);
  debugLogs.push(`Total after deduplication: ${totalAfterDedup}`);
  debugLogs.push(`Total after 4-hour age filter: ${totalAfter4h}`);
  debugLogs.push(`Total after 48-hour age filter: ${totalAfter48h}`);

  // Use the 48-hour relaxed articles for final curation
  const finalArticles = freshArticles.slice(0, 96);
  debugLogs.push(`Sending top ${finalArticles.length} unique articles (within 48 hours) to Groq curation.`);

  if (finalArticles.length === 0) {
    debugLogs.push("All source layers returned empty. No articles to curate.");
    return [];
  }

  return finalArticles;
}

