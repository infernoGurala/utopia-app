// Supabase Edge Function: fetch-daily-news
// Description: Cron-triggered function that fetches external raw news every 2 hours
//              (at 00:00, 02:00, 04:00, 06:00, 08:00, 10:00, 12:00, 14:00, 16:00, 18:00, 20:00, 22:00 UTC),
//              curates it via Groq Llama-3.3-70b-versatile, and populates the news_briefs table.

import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.38.4";

const GROQ_API_KEY = Deno.env.get("GROQ_API_KEY");
const SUPABASE_URL = Deno.env.get("DB_URL");
const SUPABASE_SERVICE_ROLE_KEY = Deno.env.get("DB_SERVICE_KEY");

const NEWS_API_KEY = Deno.env.get("NEWS_API_KEY");
const GNEWS_API_KEY = Deno.env.get("GNEWS_API_KEY");
const GUARDIAN_KEY = Deno.env.get("GUARDIAN_KEY");
const NYT_API_KEY = Deno.env.get("NYT_API_KEY");

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  // Handle CORS
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  try {
    const keys = [
      Deno.env.get("GROQ_API_KEY"),
      Deno.env.get("GROQ_API_KEY_2"),
      Deno.env.get("GROQ_API_KEY_3"),
      Deno.env.get("GROQ_API_KEY_4"),
      Deno.env.get("GROQ_API_KEY_5"),
    ].filter((k): k is string => typeof k === "string" && k.trim() !== "");

    if (keys.length === 0 || !SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
      throw new Error("Missing system environment variables (At least one GROQ_API_KEY, DB_URL, and DB_SERVICE_KEY is required).");
    }

    const debugLogs: string[] = [];
    debugLogs.push("Request started.");

    // Check optional keys
    if (!GUARDIAN_KEY) {
      debugLogs.push("Guardian API Key is missing. Guardian layer will be skipped silently.");
    }
    if (!NYT_API_KEY) {
      debugLogs.push("NYT API Key is missing. NYT layer will be skipped silently.");
    }

    console.log("Initializing Supabase Admin Client...");
    const supabase = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY);

    // Fetch news categories dynamically from DB with fallback
    const activeCategories = await fetchActiveCategories(supabase, debugLogs);

    console.log("Fetching raw articles...");
    const rawArticles = await fetchRawArticles(activeCategories, debugLogs);
    console.log(`Fetched ${rawArticles.length} raw articles to curate.`);
    debugLogs.push(`Fetched ${rawArticles.length} raw articles successfully.`);

    console.log("Screening articles via Groq...");
    const screenedArticles = await screenTitles(rawArticles, keys, debugLogs);
    console.log(`Screened articles: ${screenedArticles.length} remaining out of ${rawArticles.length}.`);
    debugLogs.push(`Screened articles: ${screenedArticles.length} remaining out of ${rawArticles.length}.`);

    console.log("Invoking Groq model for AI curation...");
    const briefings = await curateWithGroq(screenedArticles, activeCategories, debugLogs);
    console.log(`Groq generated ${briefings.length} curated news briefings.`);
    debugLogs.push(`Groq generated ${briefings.length} briefs.`);

    // Post-curation deduplication step
    const seenBriefingHeadlines = new Set<string>();
    const deduplicatedBriefings: CuratedBrief[] = [];
    let removedCount = 0;
    for (const b of briefings) {
      if (!b.headline) {
        deduplicatedBriefings.push(b);
        continue;
      }
      const normalizedHeadline = b.headline.trim().toLowerCase().replace(/[^a-z0-9]/g, "");
      if (seenBriefingHeadlines.has(normalizedHeadline)) {
        removedCount++;
      } else {
        seenBriefingHeadlines.add(normalizedHeadline);
        deduplicatedBriefings.push(b);
      }
    }
    console.log(`Deduplication removed ${removedCount} duplicate briefings.`);
    debugLogs.push(`Deduplication removed ${removedCount} duplicate briefings.`);

    // Filter out any brief where headline is empty or null/undefined, or summary is empty or null/undefined. Never insert blank articles.
    const validBriefings = deduplicatedBriefings.filter((b) => {
      const hasHeadline = b.headline && b.headline.trim() !== "";
      const hasSummary = b.summary && b.summary.trim() !== "";
      return hasHeadline && hasSummary;
    });

    console.log(`Groq generated ${briefings.length} curated news briefings. Valid briefs: ${validBriefings.length}`);
    debugLogs.push(`Groq generated ${briefings.length} briefs. Valid: ${validBriefings.length}`);

    if (validBriefings.length === 0) {
      return new Response(JSON.stringify({
        success: false,
        message: "No valid briefings generated by Groq (all were blank or missing headlines/summaries).",
        logs: debugLogs
      }), {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 200,
      });
    }

    const todayStr = new Date().toISOString().split("T")[0];
    const nowTimestamp = new Date().toISOString();

    // Purge all active news_briefs for today so we never accumulate stale/mock data
    console.log("Purging existing news_briefs for today before inserting fresh ones...");
    const { error: purgeError } = await supabase
      .from("news_briefs")
      .delete()
      .eq("fetched_date", todayStr)
      .eq("is_active", true);
    if (purgeError) {
      console.error("Failed to purge old briefs:", purgeError);
    } else {
      console.log("Successfully purged old briefs for today.");
    }

    console.log("Writing curated briefings to news_briefs table...");

    // Prepare inserts
    const inserts = validBriefings.map((b, index) => {
      const pubAt = b.published_at?.trim();
      const isNone = !pubAt || pubAt === "" || pubAt.toLowerCase() === "none";
      return {
        category: b.category,
        source_name: b.source_name,
        original_title: b.original_title,
        headline: b.headline,
        key_fact: b.key_fact,
        summary: b.summary,
        published_at: isNone ? null : pubAt,
        fetched_date: todayStr,
        fetched_at: nowTimestamp,
        display_order: index + 1,
        is_active: true,
        image_url: b.image_url || null,
      };
    });

    const { error: insertError } = await supabase.from("news_briefs").insert(inserts);

    if (insertError) {
      throw insertError;
    }

    return new Response(JSON.stringify({ success: true, count: inserts.length, data: inserts }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 200,
    });
  } catch (error) {
    console.error("Error in Edge Function execution:", error.message);
    return new Response(JSON.stringify({ success: false, error: error.message }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 500,
    });
  }
});

